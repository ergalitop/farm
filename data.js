// Уникальные категории
const CATEGORIES = [...new Set(RECIPES.map(r => r.category))].sort();

// Поиск по запросу
function searchRecipes(query, category = 'all') {
  const q = query.toLowerCase().trim();
  return RECIPES.filter(r => {
    const matchCat = category === 'all' || r.category === category;
    if (!q) return matchCat;
    return matchCat && (
      r.indication.toLowerCase().includes(q) ||
      r.drug.toLowerCase().includes(q) ||
      r.latinName.toLowerCase().includes(q) ||
      r.recipe.toLowerCase().includes(q)
    );
  });
}

// Случайный выбор N рецептов
function getRandomRecipes(n, excludeIds = []) {
  const pool = RECIPES.filter(r => !excludeIds.includes(r.id));
  const shuffled = [...pool].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, Math.min(n, shuffled.length));
}

// Получить N дистракторов (неправильных вариантов) для рецепта с данным id
function getDistractors(correctId, n = 3) {
  const others = RECIPES.filter(r => r.id !== correctId);
  return others.sort(() => Math.random() - 0.5).slice(0, n);
}
